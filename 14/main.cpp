/*
*АЛГОРИТМ
TODO 1. Сортировка массива
Отсортируем массив чисел по возрастанию. Это позволит легко находить максимальные и минимальные элементы.

TODO 2. Выбор кандидатов на максимальное произведение
Максимальное произведение трёх чисел может быть получено двумя способами:
    - Произведение трёх наибольших чисел (например, если все числа положительные).
    - Произведение двух наименьших чисел и наибольшего числа (если есть большие отрицательные числа, их произведение даёт положительное значение).

TODO 3. Определение максимального произведения
    - Найдём произведение трёх первых чисел в отсортированном массиве.
    - Найдём произведение двух последних чисел (самых маленьких, возможно отрицательных) и первого числа.
    - Возьмём максимум из этих двух вариантов.

TODO 4. Вывод результата
Выведем три числа, которые дали максимальное произведение.
*/

#include <iostream>
#include <sstream>
#include <vector>
#include <algorithm>

using namespace std;

int main()
{
    string input;
    getline(cin, input); // считываем строку

    vector<int> numbers;
    istringstream iss(input);
    int num;

    while (iss >> num)
    { // читаем числа, игнорируя пробелы
        numbers.push_back(num);
    }

    int size = numbers.size();

    // сортируем массив по убыванию для удобства работы
    sort(numbers.begin(), numbers.end(), std::greater<int>());

    // Вариант 1: 3 наибольших числа
    long long option1 = 1LL * numbers[0] * numbers[1] * numbers[2];

    // Вариант 2: 1 наибольшее число и два минимальных числа
    long long option2 = 1LL * numbers[0] * numbers[size - 1] * numbers[size - 2];

    // Определяем большее и выводим ответ
    if (option1 >= option2)
        cout << numbers[0] << " " << numbers[1] << " " << numbers[2] << endl;
    else
        cout << numbers[0] << " " << numbers[size - 1] << " " << numbers[size - 2] << endl;

    return 0;
}
